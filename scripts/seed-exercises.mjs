#!/usr/bin/env node
// Generates supabase/migrations/0004_seed_exercises.sql from
// supabase/seed/exercises.json (produced by the movements-registry build
// script from the TS exercise bank — TS registry is the source of truth).
//
// This script only WRITES the migration file; it does not run any SQL or
// touch the live database. Run it, review the generated SQL, then apply the
// migration the normal way (supabase db push / apply_migration).
//
// Usage: node scripts/seed-exercises.mjs
//
// Expected shape of each entry in exercises.json (see BUILD_SPEC.md section 1
// `ExerciseMeta` and section 2 `exercises` table):
//   {
//     slug, name, aliases[], category?, primaryMuscles[], secondaryMuscles[],
//     equipment[], difficulty, tier (1|2|3), loadType,
//     instructions[], formPoints[], commonMistakes[],
//     romGuideline?, tutTarget?: [number, number],
//     tracking?: { restAngle?, activeAngle?, minRepFraction?, view?, mode?, unilateral? }
//   }

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const inputPath = path.join(repoRoot, "supabase", "seed", "exercises.json");
const outputPath = path.join(repoRoot, "supabase", "migrations", "0004_seed_exercises.sql");

function sqlString(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlTextArray(values) {
  if (!Array.isArray(values) || values.length === 0) return "'{}'::text[]";
  const escaped = values.map((v) => `"${String(v).replace(/"/g, '\\"').replace(/'/g, "''")}"`);
  return `'{${escaped.join(",")}}'::text[]`;
}

function sqlJsonb(value) {
  const json = JSON.stringify(value ?? []);
  return `${sqlString(json)}::jsonb`;
}

function sqlInt4Range(tutTarget) {
  if (!Array.isArray(tutTarget) || tutTarget.length !== 2) return "null";
  const [lo, hi] = tutTarget;
  return `int4range(${Math.round(lo)}, ${Math.round(hi)}, '[]')`;
}

function sqlTier(tier) {
  const n = Number(tier);
  return n === 1 || n === 2 || n === 3 ? String(n) : "3";
}

function sqlDifficulty(difficulty) {
  const allowed = new Set(["beginner", "intermediate", "advanced"]);
  return sqlString(allowed.has(difficulty) ? difficulty : "beginner");
}

function sqlLoadType(loadType) {
  const allowed = new Set(["bodyweight", "external", "both"]);
  return sqlString(allowed.has(loadType) ? loadType : "bodyweight");
}

function buildRow(ex) {
  // exercises.json (export-exercises.mjs) writes snake_case keys; accept the
  // legacy camelCase form too so older seed files still convert.
  const tracking = {
    rest_angle: ex.tracking?.rest_angle ?? ex.tracking?.restAngle ?? null,
    active_angle: ex.tracking?.active_angle ?? ex.tracking?.activeAngle ?? null,
    min_rep_fraction: ex.tracking?.min_rep_fraction ?? ex.tracking?.minRepFraction ?? null,
    view: ex.tracking?.view ?? null,
    mode: ex.tracking?.mode ?? null,
    unilateral: ex.tracking?.unilateral ?? null,
  };

  return `  (
    ${sqlString(ex.slug)},
    ${sqlString(ex.name)},
    ${sqlTextArray(ex.aliases)},
    ${sqlString(ex.category ?? null)},
    ${sqlTextArray(ex.primaryMuscles)},
    ${sqlTextArray(ex.secondaryMuscles)},
    ${sqlTextArray(ex.equipment)},
    ${sqlDifficulty(ex.difficulty)},
    ${sqlTier(ex.tier)},
    ${sqlLoadType(ex.loadType)},
    ${sqlJsonb(ex.instructions)},
    ${sqlJsonb(ex.formPoints)},
    ${sqlJsonb(ex.commonMistakes)},
    ${sqlString(ex.romGuideline ?? null)},
    ${sqlInt4Range(ex.tutTarget)},
    ${sqlString(JSON.stringify(tracking))}::jsonb
  )`;
}

function main() {
  if (!existsSync(inputPath)) {
    console.error(`Missing ${path.relative(repoRoot, inputPath)}. Run the exercise-registry build script first.`);
    process.exit(1);
  }

  const raw = readFileSync(inputPath, "utf8");
  const exercises = JSON.parse(raw);
  if (!Array.isArray(exercises) || exercises.length === 0) {
    console.error("supabase/seed/exercises.json is empty or not an array — nothing to seed.");
    process.exit(1);
  }

  const rows = exercises.map(buildRow).join(",\n");

  const sql = `-- RepMint rebuild — 0004_seed_exercises.sql
-- GENERATED by scripts/seed-exercises.mjs from supabase/seed/exercises.json.
-- Do not hand-edit; re-run the script instead. Idempotent upsert on slug.

insert into public.exercises
  (slug, name, aliases, category, primary_muscles, secondary_muscles, equipment,
   difficulty, tier, load_type, instructions, form_points, common_mistakes,
   rom_guideline, tut_target, tracking)
values
${rows}
on conflict (slug) do update set
  name = excluded.name,
  aliases = excluded.aliases,
  category = excluded.category,
  primary_muscles = excluded.primary_muscles,
  secondary_muscles = excluded.secondary_muscles,
  equipment = excluded.equipment,
  difficulty = excluded.difficulty,
  tier = excluded.tier,
  load_type = excluded.load_type,
  instructions = excluded.instructions,
  form_points = excluded.form_points,
  common_mistakes = excluded.common_mistakes,
  rom_guideline = excluded.rom_guideline,
  tut_target = excluded.tut_target,
  tracking = excluded.tracking;
`;

  writeFileSync(outputPath, sql, "utf8");
  console.log(`Wrote ${path.relative(repoRoot, outputPath)} (${exercises.length} exercises).`);
  console.log("This script only writes the SQL file — review it, then apply the migration separately.");
}

main();
